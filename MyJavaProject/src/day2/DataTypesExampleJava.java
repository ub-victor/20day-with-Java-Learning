package day2;

public class DataTypesExampleJava {

	public static void main(String[] args) {
	  	// Numeric DataTypes
		byte toddlerAge = 3;
		System.out.println(toddlerAge);
		
		short bookPages = 250;
		System.out.println(bookPages);
		
		int cityPopulation = 50000;
		System.out.println(cityPopulation);
		
		long distanceToStar = 9460730472580800L;
		System.out.println(distanceToStar);
		
		float productPrice = 19.9933445f ;
		System.out.println(productPrice);
		
		double accuracy = 99.9933224567; // the can got up to 7 no need  to put d as it is traited as double
		System.out.println(accuracy);
		
		char grade = 'A';
		System.out.println(grade);
		
		boolean isValid = true;
		System.out.println(isValid);
		
		String name = "Victoire";
		System.out.println(name);

	}

}
