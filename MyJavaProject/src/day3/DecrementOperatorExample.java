package day3;

public class DecrementOperatorExample {

	public static void main(String[] args) {
		

//		int a = 10;
//		a= a-1;
//		System.out.println(a);
		
		// post decrement
		
		int a = 10;
		int result = a--;
		System.out.println(result);
		System.out.println(a);

		int b = 10;
		int resultb = b--;
		System.out.println(resultb);
		System.out.println(a);
	}

}
